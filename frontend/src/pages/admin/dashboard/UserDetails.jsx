import React, { useEffect, useState } from 'react';
import { useParams, useNavigate } from 'react-router-dom';
import './UserDetails.css';
import placeholderAvatar from '../../../assets/avatar-placeholder.svg';

const UserDetails = () => {
  const { id, lang } = useParams();
  const navigate = useNavigate();

  const [user, setUser] = useState(null);
  const [admin, setAdmin] = useState(null);
  const [showRejectModal, setShowRejectModal] = useState(false);
  const [rejectReason, setRejectReason] = useState('');

  const fetchUser = async () => {
    try {
      const res = await fetch(`${process.env.REACT_APP_API_BASE}/users/${id}`, {
        headers: {
          Authorization: `Bearer ${localStorage.getItem('adminToken')}`,
        }
      });
      const data = await res.json();
      setUser(data);
    } catch (err) {
      console.error('–û—à–∏–±–∫–∞ –∑–∞–≥—Ä—É–∑–∫–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è:', err);
    }
  };

  const fetchAdmin = async () => {
    try {
      const res = await fetch(`${process.env.REACT_APP_API_BASE}/users/me`, {
        headers: {
          Authorization: `Bearer ${localStorage.getItem('adminToken')}`,
        }
      });
      const data = await res.json();
      setAdmin(data);
    } catch (err) {
      console.error('–û—à–∏–±–∫–∞ –ø–æ–ª—É—á–µ–Ω–∏—è –∞–¥–º–∏–Ω–∞:', err);
    }
  };

  const handlePassportDecision = async (action) => {
    try {
      const url = `${process.env.REACT_APP_API_BASE}/users/${id}/${action === 'confirm' ? 'confirm-passport' : 'reject-passport'}`;
      const body = action === 'reject' ? { reason: rejectReason } : null;

      const res = await fetch(url, {
        method: 'POST',
        headers: {
          Authorization: `Bearer ${localStorage.getItem('adminToken')}`,
          'Content-Type': 'application/json',
        },
        body: body ? JSON.stringify(body) : null,
      });

      const data = await res.json();
      if (!res.ok) throw new Error(data.message || '–û—à–∏–±–∫–∞');

      fetchUser();
      setShowRejectModal(false);
      setRejectReason('');
    } catch (err) {
      alert(err.message);
    }
  };

  useEffect(() => {
    fetchUser();
    fetchAdmin();
  }, [id]);

  if (!user) return <div className="user-details">–ó–∞–≥—Ä—É–∑–∫–∞...</div>;

  return (
    <div className="user-details">
      {/* Header */}
      <div className="users-topbar">
        <h2 className="page-title">–î–µ—Ç–∞–ª–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è</h2>
        <div className="admin-info">
          <span>{admin?.firstName || '–ê–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä'}</span>
          <img
            src={admin?.avatar?.startsWith('/uploads/')
              ? `${process.env.REACT_APP_API_BASE.replace('/api', '')}${admin.avatar}`
              : placeholderAvatar}
            alt="admin"
            className="admin-avatar"
          />
        </div>
      </div>

      {/* –ù–∞–∑–∞–¥ */}
      <button className="user-back" onClick={() => navigate(`/${lang}/admin/dashboard/users`)}>
        ‚Üê –ù–∞–∑–∞–¥ –∫ —Å–ø–∏—Å–∫—É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π
      </button>

      {/* –§–æ—Ç–æ + –§–ò–û */}
      <div className="user-header">
        <img
          src={user.avatar?.startsWith('/uploads/')
            ? `${process.env.REACT_APP_API_BASE.replace('/api', '')}${user.avatar}`
            : placeholderAvatar}
          alt="avatar"
          className="user-avatar"
        />
        <h2>{user.firstName || '-'} {user.lastName || ''}</h2>
      </div>

      {/* –î–µ—Ç–∞–ª–∏ */}
      <div className="user-info">
        <p><strong>–¢–µ–ª–µ—Ñ–æ–Ω:</strong> {user.phone || '-'}</p>
        <p>
          <strong>–ü–æ–¥—Ç–≤–µ—Ä–∂–¥–µ–Ω–∏–µ —Ç–µ–ª–µ—Ñ–æ–Ω–∞:</strong>{' '}
          <span className={user.isPhoneVerified ? 'text-yes' : 'text-no'}>
            {user.isPhoneVerified ? '–î–∞' : '–ù–µ—Ç'}
          </span>
        </p>
        <p><strong>–ü–æ—á—Ç–∞:</strong> {user.email}</p>
        <p>
          <strong>–ü–æ–¥—Ç–≤–µ—Ä–∂–¥–µ–Ω–∏–µ –ø–æ—á—Ç—ã:</strong>{' '}
          <span className={user.isEmailVerified ? 'text-yes' : 'text-no'}>
            {user.isEmailVerified ? '–î–∞' : '–ù–µ—Ç'}
          </span>
        </p>
        <p><strong>–î–∞—Ç–∞ —Ä–æ–∂–¥–µ–Ω–∏—è:</strong> {user.birthDate ? new Date(user.birthDate).toLocaleDateString() : '-'}</p>
        <p><strong>–û —Å–µ–±–µ:</strong> {user.about || '-'}</p>
        <p>
          <strong>–í–æ–¥–∏—Ç–µ–ª—å:</strong>{' '}
          <span className={user.isDriver ? 'text-yes' : 'text-no'}>
            {user.isDriver ? '–î–∞' : '–ù–µ—Ç'}
          </span>
        </p>
        <p>
          <strong>–ü–æ–¥—Ç–≤–µ—Ä–∂–¥–µ–Ω–∏–µ —Å—Ç–∞—Ç—É—Å–∞ –≤–æ–¥–∏—Ç–µ–ª—è:</strong>{' '}
          <span className={user.isVerified ? 'text-yes' : 'text-no'}>
            {user.isVerified ? '–î–∞' : '–ù–µ—Ç'}
          </span>
        </p>
        <p><strong>–†–æ–ª—å:</strong> {user.role}</p>
        <p><strong>–ó–∞—Ä–µ–≥–∏—Å—Ç—Ä–∏—Ä–æ–≤–∞–Ω:</strong> {new Date(user.createdAt).toLocaleDateString()}</p>

        {/* –ü–∞—Å–ø–æ—Ä—Ç */}
        <p>
          <strong>–ü–∞—Å–ø–æ—Ä—Ç:</strong>{' '}
          {user.passportFile ? (
            <>
              <a
                href={`${process.env.REACT_APP_API_BASE.replace('/api', '')}${user.passportFile}`}
                target="_blank"
                rel="noreferrer"
                className="link-download"
              >
                üìÑ –°–∫–∞—á–∞—Ç—å
              </a>
              {' '}
              {user.passportStatus === 'pending' && (
                <>
                  <button className="btn-confirm" onClick={() => handlePassportDecision('confirm')}>–ü–æ–¥—Ç–≤–µ—Ä–¥–∏—Ç—å</button>
                  <button className="btn-reject" onClick={() => setShowRejectModal(true)}>–û—Ç–∫–ª–æ–Ω–∏—Ç—å</button>
                </>
              )}
              {user.passportStatus === 'confirmed' && <span className="text-yes">–ü–æ–¥—Ç–≤–µ—Ä–∂–¥—ë–Ω</span>}
              {user.passportStatus === 'rejected' && <span className="text-no">–û—Ç–∫–ª–æ–Ω—ë–Ω: {user.passportRejectReason}</span>}
            </>
          ) : (
            <span>-</span>
          )}
        </p>
      </div>

      {/* –ú–æ–¥–∞–ª–∫–∞ –æ—Ç–∫–ª–æ–Ω–µ–Ω–∏—è */}
      {showRejectModal && (
        <div className="modal-overlay">
          <div className="modal-content">
            <h3>–ü—Ä–∏—á–∏–Ω–∞ –æ—Ç–∫–ª–æ–Ω–µ–Ω–∏—è</h3>
            <textarea
              value={rejectReason}
              onChange={(e) => setRejectReason(e.target.value)}
              placeholder="–ù–∞–ø—Ä–∏–º–µ—Ä: –Ω–µ—á—ë—Ç–∫–æ–µ —Ñ–æ—Ç–æ"
            />
            <div className="modal-buttons">
              <button onClick={() => handlePassportDecision('reject')}>–û—Ç–∫–ª–æ–Ω–∏—Ç—å</button>
              <button onClick={() => setShowRejectModal(false)}>–û—Ç–º–µ–Ω–∞</button>
            </div>
          </div>
        </div>
      )}
    </div>
  );
};

export default UserDetails;
